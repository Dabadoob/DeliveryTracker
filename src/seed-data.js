function csvToArray(csv) {
  const rows = csv.split('\n').slice(1) // split on newlines, don't take header row.
  return rows.map(r => {
    const row = r.split(',')
    return {
      date: row[0],
      hoursWorked: parseFloat(row[1]),
      deliveriesCompleted: parseFloat(row[2]),
      commissionEarned: parseFloat(row[3]),
      tipsEarned: parseFloat(row[4]),
      cashEarned: parseFloat(row[5]),
      boost: parseFloat(row[6]),
      bonus: parseFloat(row[7]),
      endOfWeekSubsidyAdded: parseFloat(row[8]),
      other: parseFloat(row[9]),
      milesDriven: parseFloat(row[10]),
    }
  })
}

